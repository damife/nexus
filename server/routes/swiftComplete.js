/**
 * Get message type details
 */
router.get('/details/:messageType', authenticateToken, async (req, res) => {
  try {
    const { messageType } = req.params;
    const allMessageTypes = swiftGenerator.getAllMessageTypes();

    const mtType = allMessageTypes.mt[messageType];
    const mxType = allMessageTypes.mx[messageType];

    if (!mtType && !mxType) {
      return res.status(404).json({
        success: false,
        error: `Message type ${messageType} not found`
      });
    }

    res.json({
      success: true,
      data: {
        messageType,
        description: mtType || mxType,
        type: mtType ? 'MT' : 'MX',
        category: mtType ? getCategoryFromMessageType(messageType) : 'MX',
        requiredFields: getRequiredFields(messageType),
        sampleFields: getSampleFields(messageType)
      }
    });

  } catch (error) {
    logger.error(`Error getting SWIFT message details for ${req.params.messageType}:`, error);
    res.status(500).json({
      success: false,
      error: 'Failed to get SWIFT message details'
    });
  }
});

