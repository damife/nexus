// Helper functions
function getRequiredFields(messageType) {
  const fieldMap = {
    'MT103': ['senderBIC', 'receiverBIC', 'transactionReference', 'currency', 'amount', 'orderingCustomer', 'beneficiaryCustomer'],
    'MT199': ['senderBIC', 'receiverBIC', 'transactionReference', 'messageContent'],
    'MT940': ['senderBIC', 'receiverBIC', 'accountNumber', 'statementNumber', 'openingBalance', 'closingBalance'],
    'MT202': ['senderBIC', 'receiverBIC', 'transactionReference', 'currency', 'amount'],
    'MT300': ['senderBIC', 'receiverBIC', 'transactionReference', 'currency', 'amount', 'exchangeRate'],
    'MT320': ['senderBIC', 'receiverBIC', 'transactionReference', 'currency', 'amount', 'interestRate'],
    'MT540': ['senderBIC', 'receiverBIC', 'transactionReference', 'securityIdentification', 'quantity'],
    'MT541': ['senderBIC', 'receiverBIC', 'transactionReference', 'securityIdentification', 'quantity'],
    'MT542': ['senderBIC', 'receiverBIC', 'transactionReference', 'securityIdentification', 'quantity', 'settlementAmount'],
    'MT700': ['senderBIC', 'receiverBIC', 'transactionReference', 'documentaryCreditNumber', 'applicant', 'beneficiary'],
    'MT710': ['senderBIC', 'receiverBIC', 'transactionReference', 'documentaryCreditNumber', 'issuingBank'],
    'MT799': ['senderBIC', 'receiverBIC', 'transactionReference', 'messageContent'],
    'MT910': ['senderBIC', 'receiverBIC', 'transactionReference', 'accountNumber', 'amount'],
    'MT950': ['senderBIC', 'receiverBIC', 'transactionReference', 'accountNumber', 'statementData']
  };

  return fieldMap[messageType] || ['senderBIC', 'receiverBIC', 'transactionReference'];
}

