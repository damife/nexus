/**
 * Complete SWIFT Message Routes for SwiftNexus Enterprise
 * All MT Message Types (0-9 Categories) + MX Messages
 */

import express from 'express';
import rateLimit from 'express-rate-limit';
import SwiftMessageGenerator from '../utils/swiftMessageGeneratorEnhanced.js';
import { authenticateToken, requireRole } from '../middleware/auth.js';
import logger from '../config/logger.js';

const router = express.Router();
const swiftGenerator = new SwiftMessageGenerator();

// Rate limiting for SWIFT operations
const swiftLimiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 minutes
  max: 50, // Limit each IP to 50 SWIFT requests per windowMs
  message: {
    error: 'Too many SWIFT requests, please try again later',
    retryAfter: '15 minutes'
  }
});

// Apply rate limiting to all SWIFT routes
router.use(swiftLimiter);

/**
 * Generate any SWIFT MT message by type
 */
router.post('/generate/:messageType', authenticateToken, requireRole(['admin', 'user']), async (req, res) => {
  try {
    const { messageType } = req.params;
    const messageData = req.body;

    // Validate message type exists
    const allMessageTypes = swiftGenerator.getAllMessageTypes();
    if (!allMessageTypes.mt[messageType]) {
      return res.status(400).json({
        success: false,
        error: `Invalid message type: ${messageType}`,
        availableTypes: Object.keys(allMessageTypes.mt)
      });
    }

    // Validate required fields based on message type
    const requiredFields = getRequiredFields(messageType);
    const missing = requiredFields.filter(field => !messageData[field]);
    
    if (missing.length > 0) {
      return res.status(400).json({
        success: false,
        error: 'Missing required fields',
        missing: missing,
        required: requiredFields
      });
    }

    // Generate message based on type
    let swiftMessage;
    switch (messageType) {
      case 'MT103':
        swiftMessage = swiftGenerator.generateMT103(messageData);
        break;
      case 'MT199':
        swiftMessage = swiftGenerator.generateMT199(messageData);
        break;
      case 'MT940':
        swiftMessage = swiftGenerator.generateMT940(messageData);
        break;
      case 'MT202':
        swiftMessage = swiftGenerator.generateMT202(messageData);
        break;
      case 'MT300':
        swiftMessage = swiftGenerator.generateMT300(messageData);
        break;
      case 'MT320':
        swiftMessage = swiftGenerator.generateMT320(messageData);
        break;
      case 'MT540':
        swiftMessage = swiftGenerator.generateMT540(messageData);
        break;
      case 'MT541':
        swiftMessage = swiftGenerator.generateMT541(messageData);
        break;
      case 'MT542':
        swiftMessage = swiftGenerator.generateMT542(messageData);
        break;
      case 'MT700':
        swiftMessage = swiftGenerator.generateMT700(messageData);
        break;
      case 'MT710':
        swiftMessage = swiftGenerator.generateMT710(messageData);
        break;
      case 'MT799':
        swiftMessage = swiftGenerator.generateMT799(messageData);
        break;
      case 'MT910':
        swiftMessage = swiftGenerator.generateMT910(messageData);
        break;
      case 'MT950':
        swiftMessage = swiftGenerator.generateMT950(messageData);
        break;
      default:
        // Generate generic message for other types
        swiftMessage = generateGenericMessage(messageType, messageData);
        break;
    }

    // Validate generated message
    const validation = swiftGenerator.validateSwiftMessage(swiftMessage);
    if (!validation.isValid) {
      return res.status(500).json({
        success: false,
        error: 'Generated SWIFT message validation failed',
        validationErrors: validation.errors
      });
    }

    logger.info(`SWIFT ${messageType} generated by user ${req.user.id}`);

    res.json({
      success: true,
      data: {
        messageType,
        messageDescription: allMessageTypes.mt[messageType],
        swiftMessage,
        validation,
        category: getCategoryFromMessageType(messageType)
      }
    });

  } catch (error) {
    logger.error(`Error generating SWIFT ${req.params.messageType}:`, error);
    res.status(500).json({
      success: false,
      error: `Failed to generate SWIFT ${req.params.messageType} message`
    });
  }
});

/**
 * Generate MX Message (ISO 20022)
 */
router.post('/generate/mx/:messageType', authenticateToken, requireRole(['admin', 'user']), async (req, res) => {
  try {
    const { messageType } = req.params;
    const messageData = req.body;

    // Validate MX message type exists
    const allMessageTypes = swiftGenerator.getAllMessageTypes();
    if (!allMessageTypes.mx[messageType]) {
      return res.status(400).json({
        success: false,
        error: `Invalid MX message type: ${messageType}`,
        availableTypes: Object.keys(allMessageTypes.mx)
      });
    }

    let mxMessage;
    switch (messageType) {
      case 'pacs.008':
        mxMessage = swiftGenerator.generatePacs008(messageData);
        break;
      case 'camt.052':
        mxMessage = swiftGenerator.generateCamt052(messageData);
        break;
      default:
        mxMessage = swiftGenerator.generateMXMessage(messageType, messageData);
        break;
    }

    logger.info(`SWIFT MX ${messageType} generated by user ${req.user.id}`);

    res.json({
      success: true,
      data: {
        messageType,
        messageDescription: allMessageTypes.mx[messageType],
        mxMessage,
        format: 'ISO 20022 XML'
      }
    });

  } catch (error) {
    logger.error(`Error generating SWIFT MX ${req.params.messageType}:`, error);
    res.status(500).json({
      success: false,
      error: `Failed to generate SWIFT MX ${req.params.messageType} message`
    });
  }
});

/**
 * Convert MT to MX message
 */
router.post('/convert/mt-to-mx', authenticateToken, requireRole(['admin', 'user']), async (req, res) => {
  try {
    const { mtMessage, messageType } = req.body;

    if (!mtMessage || !messageType) {
      return res.status(400).json({
        success: false,
        error: 'MT message and message type are required'
      });
    }

    const mxMessage = swiftGenerator.convertMTtoMX(mtMessage, messageType);

    logger.info(`SWIFT ${messageType} converted to MX by user ${req.user.id}`);

    res.json({
      success: true,
      data: {
        originalMessageType: messageType,
        convertedToMX: mxMessage,
        mtMessage,
        conversionType: 'ISO 20022 Migration'
      }
    });

  } catch (error) {
    logger.error('Error converting MT to MX:', error);
    res.status(500).json({
      success: false,
      error: 'Failed to convert MT to MX message'
    });
  }
});

