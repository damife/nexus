function getCategoryFromMessageType(messageType) {
  if (messageType.startsWith('MT')) {
    return messageType.substring(2, 3); // Get the third digit
  }
  return 'MX';
}

function generateGenericMessage(messageType, messageData) {
  // Generate a generic SWIFT message for unsupported types
  const { senderBIC, receiverBIC, transactionReference, ...otherFields } = messageData;
  
  const basicHeader = swiftGenerator.generateBasicHeader(senderBIC);
  const applicationHeader = swiftGenerator.generateApplicationHeader(messageType, receiverBIC);
  const userHeader = swiftGenerator.generateUserHeader();
  
  const textFields = {
    '20': transactionReference,
    ...otherFields
  };
  
  const textBlock = swiftGenerator.generateTextBlock(textFields);
  const trailer = swiftGenerator.generateTrailer();

  return `${basicHeader}\n${applicationHeader}\n${userHeader}\n${textBlock}\n${trailer}`;
}

