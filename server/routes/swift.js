/**
 * Validate SWIFT message
 */
router.post('/validate', authenticateToken, requireRole(['admin', 'user']), async (req, res) => {
  try {
    const { swiftMessage } = req.body;

    if (!swiftMessage) {
      return res.status(400).json({
        success: false,
        error: 'SWIFT message is required'
      });
    }

    const validation = swiftGenerator.validateSwiftMessage(swiftMessage);

    res.json({
      success: true,
      data: {
        validation,
        parsedMessage: swiftGenerator.parseSwiftMessage(swiftMessage)
      }
    });

  } catch (error) {
    logger.error('Error validating SWIFT message:', error);
    res.status(500).json({
      success: false,
      error: 'Failed to validate SWIFT message'
    });
  }
});

